package org.geotools.data.duckdb;

import java.sql.Connection;
import java.sql.PreparedStatement;
import java.sql.ResultSet;
import java.sql.ResultSetMetaData;
import java.sql.SQLException;
import java.sql.Statement;
import java.util.ArrayList;
import java.util.LinkedHashMap;
import java.util.List;
import java.util.Locale;
import java.util.Map;
import org.geotools.jdbc.AutoGeneratedPrimaryKeyColumn;
import org.geotools.jdbc.JDBCDataStore;
import org.geotools.jdbc.NonIncrementingPrimaryKeyColumn;
import org.geotools.jdbc.PrimaryKey;
import org.geotools.jdbc.PrimaryKeyColumn;
import org.geotools.jdbc.PrimaryKeyFinder;
import org.geotools.jdbc.SQLDialect;
import org.geotools.jdbc.SequencedPrimaryKeyColumn;

/**
 * DuckDB-specific primary key finder that avoids nested JDBC metadata cursors.
 *
 * <p>DuckDB can invalidate active metadata result sets when additional metadata calls are issued on the same
 * connection. The generic heuristic finder relies on that pattern, so this implementation uses catalog SQL queries
 * instead.
 */
final class DuckDBPrimaryKeyFinder extends PrimaryKeyFinder {

    @Override
    public PrimaryKey getPrimaryKey(JDBCDataStore store, String schema, String table, Connection cx)
            throws SQLException {
        List<String> primaryKeyColumns = lookupPrimaryKeyColumns(schema, table, cx);
        if (primaryKeyColumns.isEmpty()) {
            return null;
        }

        Map<String, ColumnMetadata> columnsByName = lookupColumnMetadata(store, schema, table, cx);
        List<PrimaryKeyColumn> primaryKey = new ArrayList<>(primaryKeyColumns.size());
        SQLDialect dialect = store.getSQLDialect();
        for (String columnName : primaryKeyColumns) {
            ColumnMetadata metadata = lookupColumnMetadata(columnsByName, columnName);
            Class<?> binding = metadata != null ? metadata.binding : Object.class;
            String sequence = dialect.getSequenceForColumn(schema, table, columnName, cx);
            if (sequence != null && !sequence.isBlank()) {
                primaryKey.add(new SequencedPrimaryKeyColumn(columnName, binding, sequence));
            } else if (metadata != null && metadata.autoIncrement) {
                primaryKey.add(new AutoGeneratedPrimaryKeyColumn(columnName, binding));
            } else {
                primaryKey.add(new NonIncrementingPrimaryKeyColumn(columnName, binding));
            }
        }

        return new PrimaryKey(table, primaryKey);
    }

    private List<String> lookupPrimaryKeyColumns(String schema, String table, Connection cx) throws SQLException {
        StringBuilder sql = new StringBuilder();
        sql.append("SELECT kcu.column_name ");
        sql.append("FROM information_schema.table_constraints tc ");
        sql.append("JOIN information_schema.key_column_usage kcu ");
        sql.append("ON tc.constraint_catalog = kcu.constraint_catalog ");
        sql.append("AND tc.constraint_schema = kcu.constraint_schema ");
        sql.append("AND tc.constraint_name = kcu.constraint_name ");
        sql.append("AND tc.table_name = kcu.table_name ");
        sql.append("WHERE tc.constraint_type = 'PRIMARY KEY' ");
        sql.append("AND tc.table_name = ?");
        if (hasText(schema)) {
            sql.append(" AND tc.table_schema = ?");
        }
        sql.append(" ORDER BY kcu.ordinal_position");

        try (PreparedStatement ps = cx.prepareStatement(sql.toString())) {
            ps.setString(1, table);
            if (hasText(schema)) {
                ps.setString(2, schema);
            }

            try (ResultSet rs = ps.executeQuery()) {
                List<String> columns = new ArrayList<>();
                while (rs.next()) {
                    columns.add(rs.getString(1));
                }
                return columns;
            }
        }
    }

    private Map<String, ColumnMetadata> lookupColumnMetadata(
            JDBCDataStore store, String schema, String table, Connection cx) throws SQLException {
        SQLDialect dialect = store.getSQLDialect();
        StringBuffer sql = new StringBuffer("SELECT * FROM ");
        if (hasText(schema)) {
            dialect.encodeSchemaName(schema, sql);
            sql.append('.');
        }
        dialect.encodeTableName(table, sql);
        sql.append(" WHERE 1 = 0");

        Map<String, ColumnMetadata> metadata = new LinkedHashMap<>();
        try (Statement statement = cx.createStatement();
                ResultSet rs = statement.executeQuery(sql.toString())) {
            ResultSetMetaData rsMeta = rs.getMetaData();
            for (int i = 1; i <= rsMeta.getColumnCount(); i++) {
                String name = rsMeta.getColumnLabel(i);
                Class<?> binding = store.getMapping(rsMeta.getColumnType(i));
                if (binding == null) {
                    binding = Object.class;
                }
                ColumnMetadata columnMetadata = new ColumnMetadata(binding, rsMeta.isAutoIncrement(i));
                metadata.put(name, columnMetadata);
                metadata.put(name.toLowerCase(Locale.ROOT), columnMetadata);
            }
        }
        return metadata;
    }

    private ColumnMetadata lookupColumnMetadata(Map<String, ColumnMetadata> columnsByName, String columnName) {
        ColumnMetadata metadata = columnsByName.get(columnName);
        if (metadata != null) {
            return metadata;
        }
        return columnsByName.get(columnName.toLowerCase(Locale.ROOT));
    }

    private boolean hasText(String value) {
        return value != null && !value.isBlank();
    }

    @Override
    public String toString() {
        return "DuckDBPrimaryKeyFinder";
    }

    private static final class ColumnMetadata {
        final Class<?> binding;
        final boolean autoIncrement;

        private ColumnMetadata(Class<?> binding, boolean autoIncrement) {
            this.binding = binding;
            this.autoIncrement = autoIncrement;
        }
    }
}
